// 1 - Crie uma função que retorna o dano do dragão.

const dragonAttack = (dragon) => {
  const attackMin = 15;
  const dragonDmg = Math.floor((Math.random() * (dragon. strength - attackMin + 1) + attackMin));
  return dragonDmg;
};  

